#include <Pds/Vector>


////////////////////////////////////////////////////////////////////////

Pds::Vector Pds::Vector::Unit(void) const
{
    if(this->IsEmpty()) return Pds::Vector();
    Pds::Vector U=(*this);
    return U/(this->Norm());
}

